# Leemos dato como tipo 'int' (numero entero)
numero = int(input())

# Calculamos antecesor y sucesor del numero leido
antecesor = numero - 1
sucesor = numero + 1

# Imprimos antecesor y sucesor del numero
print(f"El antecesor de {numero} es {antecesor}")
print(f"El sucesor de {numero} es {sucesor}")

